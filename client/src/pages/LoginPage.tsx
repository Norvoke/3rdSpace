import { useEffect } from 'react';
import { useNavigate, useSearchParams } from 'react-router-dom';
import { useAuthStore } from '../store/authStore';
import styles from './LoginPage.module.css';

export default function LoginPage() {
  const { isAuthenticated } = useAuthStore();
  const navigate = useNavigate();
  const [searchParams] = useSearchParams();
  const error = searchParams.get('error');

  useEffect(() => {
    if (isAuthenticated) navigate('/feed', { replace: true });
  }, [isAuthenticated]);

  const handleGoogleLogin = () => {
    window.location.href = '/auth/google';
  };

  return (
    <div className={styles.page}>
      <div className={styles.card}>
        <div className={styles.logo}>
          <span className={styles.logoAccent}>3rd</span>Space
        </div>
        <p className={styles.tagline}>
          A place for your people.<br />
          Non-profit. No ads. No algorithm.
        </p>
        <div className={styles.divider} />
        {error && (
          <p className={styles.error}>Sign-in failed. Please try again.</p>
        )}
        <button onClick={handleGoogleLogin} className={styles.googleBtn}>
          <GoogleIcon />
          Continue with Google
        </button>
        <p className={styles.footnote}>
          Free to use. Always will be.
        </p>
      </div>
      <div className={styles.marqueeWrap} aria-hidden="true">
        <div className={styles.marquee}>
          {[...Array(8)].map((_, i) => (
            <span key={i}>3rdSpace &nbsp;·&nbsp; non-profit &nbsp;·&nbsp; no ads &nbsp;·&nbsp; chronological &nbsp;·&nbsp; for your friends &nbsp;·&nbsp; </span>
          ))}
        </div>
      </div>
    </div>
  );
}

function GoogleIcon() {
  return (
    <svg width="18" height="18" viewBox="0 0 18 18" fill="none">
      <path d="M17.64 9.2c0-.637-.057-1.251-.164-1.84H9v3.481h4.844c-.209 1.125-.843 2.078-1.796 2.716v2.259h2.908c1.702-1.567 2.684-3.875 2.684-6.615z" fill="#4285F4"/>
      <path d="M9 18c2.43 0 4.467-.806 5.956-2.184l-2.908-2.259c-.806.54-1.837.86-3.048.86-2.344 0-4.328-1.584-5.036-3.711H.957v2.332C2.438 15.983 5.482 18 9 18z" fill="#34A853"/>
      <path d="M3.964 10.706A5.41 5.41 0 0 1 3.682 9c0-.593.102-1.17.282-1.706V4.962H.957A8.996 8.996 0 0 0 0 9c0 1.452.348 2.827.957 4.038l3.007-2.332z" fill="#FBBC05"/>
      <path d="M9 3.58c1.321 0 2.508.454 3.44 1.345l2.582-2.58C13.463.891 11.426 0 9 0 5.482 0 2.438 2.017.957 4.962L3.964 7.294C4.672 5.163 6.656 3.58 9 3.58z" fill="#EA4335"/>
    </svg>
  );
}
